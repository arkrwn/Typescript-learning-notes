// Arithmetic Operators
// Addition (+)
var x = 10;
var y = 5;
var sum = x + y; // sum will be 15
// Subtraction (-)
var difference = x - y; // difference will be 5
// Multiplication (*)
var product = x * y; // product will be 50
// Division (/)
var quotient = x / y; // quotient will be 2
// Modulus (Remainder) (%)
var remainder = x % y; // remainder will be 0
// Exponentiation (**)
var power = Math.pow(x, 2); // power will be 100 (10 raised to the power of 2)
// Increment (++)
var num = 5;
num++; // num will be 6
// Decrement (--)
num--; // num will be 5 again
