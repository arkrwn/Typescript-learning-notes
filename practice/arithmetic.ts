let numberX: number = 10;
let numberY: number = 22;
let numberZ: number = 3.5;

let sumXY = numberX + numberY; // Sum of two integers, result is also an integer
console.log("Sum Result " + sumXY);

let divideYZ = numberY / numberZ ; // Division of two numbers with decimal places,
console.log("Divide Y and Z, result " + divideYZ);

let  remainderXY = numberX % numberY; // Remainder/modulus operation on division between two integers, the result is always an integer
console.log("Remainder X and Y, result " + remainderXY)

