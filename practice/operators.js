var booleanResultAnd = true && false; // Logical AND returns TRUE only if both operands are TRUE
console.log("Logical AND Result " + booleanResultAnd);
var booleanResultOr = true || false; // Logical OR returns TRUE if either one or both operands are TRUE
console.log("Logical OR Result " + booleanResultOr);
var booleanNotTrue = !booleanResultAnd; // Logical NOT inverts the value of a single operand (TRUE becomes FALSE)
