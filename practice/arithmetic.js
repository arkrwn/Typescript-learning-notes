var numberX = 10;
var numberY = 22;
var numberZ = 3.5;
var sumXY = numberX + numberY; // Sum of two integers, result is also an integer
console.log("Sum Result " + sumXY);
var divideYZ = numberY / numberZ; // Division of two numbers with decimal places,
console.log("Divide Y and Z, result " + divideYZ);
var remainderXY = numberX % numberY; // Remainder/modulus operation on division between
// two integers, the result is always an integer
console.log("Remainder X and Y, result " + remainderXY);
